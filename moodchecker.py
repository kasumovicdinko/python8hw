print("Hello!")

mood = input("How is your mood today? (happy, sad, excited, nervous, relaxed) ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "sad":
    print("It is okay to feel sad!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "excited":
    print("Yay! Good for you!")
elif mood == "relaxed":
    print("Way to go!")
else:
    print("I do not recognize this mood.")
